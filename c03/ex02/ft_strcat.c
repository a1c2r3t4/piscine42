char	*ft_strcat(char *dest, char *src)
{
	char *a;
	char *b;

	a = src;
	b = dest;
	while (*b)
		b++;
	while (*a)
	{
		*b = *a;
		a++;
		b++;
	}
	*b = 0;
	return (dest);
}
