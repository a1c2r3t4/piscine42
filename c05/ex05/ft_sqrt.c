int		ft_sqrt(int nb)
{
	long a;

	a = 1;
	if (nb <= 0 || nb == 2)
		return (0);
	while (a * a < nb)
		a++;
	if ((nb % a) == 0)
		return (a);
	else
		return (0);
}
